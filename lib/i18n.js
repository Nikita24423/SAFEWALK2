/**
 * @param {Locale} locale
 * @param {string} key
 */
export function translate(locale, key) {
  const pack = messages[locale] || messages[DEFAULT_LOCALE];
  return pack[key] ?? messages[DEFAULT_LOCALE][key] ?? key;
}

const ROLE_TO_SITUATION_KEY = {
  late_walk: "situationLateWalk",
  stranger_date: "situationStrangerDate",
  unknown_place: "situationUnknownPlace",
  unplanned_meeting: "situationUnplannedMeeting",
};

/** @param {string} role */
export function situationMessageKey(role) {
  return ROLE_TO_SITUATION_KEY[role] || "situationLateWalk";
}
